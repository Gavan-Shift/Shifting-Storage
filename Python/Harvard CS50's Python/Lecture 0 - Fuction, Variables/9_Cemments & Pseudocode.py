# 주석(### 또는 #)은 사람이 읽기 위한 코드 설명
# 의사코드(pseudocode)는 실제 문법 대신 논리 중심으로 흐름을 설명


# 기호 : #, """, '''
# 코드를 손상시키지 않고 메모 작성 가능
# (User-Human만 인지, 컴퓨터는 이를 무시하고 실행)


#은 한 줄씩 주석처리하기에 용이
# Ask user for their name

name = input("What's your name? ")

# Say hello to user
print("hello, " )
print(name)

#######################################################################

# """ / '''로 묶으면 범위 단위의 주석처리가 가능
"""
Ask user for their name
Say hello to user
"""
name = input("What's your name? ")
print("hello, " )
print(name)

